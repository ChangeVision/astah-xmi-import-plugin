package com.change_vision.astah.xmi.internal.convert.exception;

public class ContentsEmptyException extends RuntimeException {

    private static final long serialVersionUID = -8033579280927980360L;

    public ContentsEmptyException() {
        super();
    }

    public ContentsEmptyException(String message, Throwable cause) {
        super(message, cause);
    }

    public ContentsEmptyException(String message) {
        super(message);
    }

    public ContentsEmptyException(Throwable cause) {
        super(cause);
    }
    
}
